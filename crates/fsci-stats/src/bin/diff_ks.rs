use fsci_stats::*;
fn main() {
    let cases: [(&[f64], &[f64]); 4] = [
        (&[1., 2., 3., 4.], &[1.5, 2.5, 3.5, 4.5]),
        (&[1., 2., 3., 4., 5.], &[2., 4., 6., 8., 10.]),
        (&[1., 3., 5., 7., 9.], &[2., 4., 6., 8., 10.]),
        (
            &[1., 2., 3., 4., 5., 6., 7.],
            &[1.5, 2.5, 8., 9., 10., 11., 12.],
        ),
    ];
    for (i, (x, y)) in cases.iter().enumerate() {
        let r = ks_2samp(x, y);
        println!("ks{i},{:.17e},{:.17e}", r.statistic, r.pvalue);
    }
}
