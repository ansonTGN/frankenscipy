use fsci_signal::{SosSection, sosfilt_zi};
fn main() {
    let sos: Vec<SosSection> = vec![
        [
            0.004824343357716228,
            0.009648686715432456,
            0.004824343357716228,
            1.0,
            -1.0485995763626117,
            0.2961403575616696,
        ],
        [1.0, 2.0, 1.0, 1.0, -1.3209134308194264, 0.6327387928852766],
    ];
    for z in sosfilt_zi(&sos).unwrap() {
        println!("{:.17e};{:.17e}", z[0], z[1]);
    }
}
